
package ejercicios;

public class Ejercicios {

    public static void main(String[] args) {
        //Operadores
        int varNum1 = 1;
        
        varNum1 += 2;
        System.out.println("+= " + varNum1);
        
        varNum1 -= 1;
        System.out.println("-= "+ varNum1);
        
        varNum1 *= 4;
        System.out.println("*= "+ varNum1);
        
        varNum1 /= 2;
        System.out.println("/= "+ varNum1);
        
        varNum1 %=2;
        System.out.println("%= "+ varNum1);
    }
    
}
